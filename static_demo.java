package com.variable;

public class static_demo 
{
	static String name ="praveen";
	static int age =17;
	static double salary = 25000;
	
	
	public static void main(String args[])
	{
		System.out.println("static variable name :" + static_demo.name);
		System.out.println("static variable age :" + static_demo.age);
		System.out.println("static variable salary :" + static_demo.salary);
	}
}
